def handle_artascii_command(art_type, client):
    if art_type in ASCII_ARTS:
        art = ASCII_ARTS[art_type]
        client.publish("GHBot/to/irc/channel/privmsg", f"ASCII Art:\n{art}")
    else:
        client.publish("GHBot/to/irc/channel/privmsg", "Unknown ASCII art type.")

